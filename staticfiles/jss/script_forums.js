$(document).ready(function(){


	$('.button').mouseenter(function(){
		$(this).css('background', 'linear-gradient(#F5DC8A 50%, white)');

	});
	$('.button').mouseleave(function(){
		$(this).css('background', 'linear-gradient(white, #F5DC8A 50%)');

	});
	
	
	
	$('.thread_buttons').mouseenter(function(){
		$(this).css('background', 'linear-gradient(#F5DC8A 50%, white)');

	});

	$('.thread_buttons').mouseleave(function(){
		$(this).css('background', 'linear-gradient(white, #F5DC8A 50%)');

	});
	
});